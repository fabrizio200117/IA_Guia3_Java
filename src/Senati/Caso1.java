package Senati;

public class Caso1 {
	public static void main(String[] args) {
	    // Aumente de 1 a 5 
		System.out.println("Creciente");
		System.out.println("===============");
		
		int i = 1;
		
		while (i <= 5) {
			System.out.println("Mensaje " + i);
			i ++;
		}
		
		// Decreciente de 5 a 1	
		System.out.println("Decreciente");
		System.out.println("===============");
		
		int j = 5;
		
		while (j >= 1) {
			System.out.println("Mensaje " + j);
			j--;
		}	
	}
		

}
